function params = build_benchmark_poiseuille_params_equal(overrides)
%BUILD_BENCHMARK_POISEUILLE_PARAMS_EQUAL
% Params aligned with the MATLAB reference runOut.params exported in export.mat.
%
% Reference values used here:
%   caseType = 'poiseuille'
%   Lx = Ly = 10
%   Nx = Ny = 50
%   gamma = 20
%   n = 50000
%   dt = 5e-3
%   nSteps = 2000
%   alphaDeg = 170
%   kBT = 1
%   g = 0
%   bodyForceX = 0.1
%   periodic x, thermalize y
%   zone tiles 25 x 25
%   useZoneRedistribution = true
%   useLiquidClosure = true
%   smoothPdriveAtInterzone = true
%
% Any field in overrides replaces the aligned default.

if nargin < 1 || isempty(overrides)
    overrides = struct();
end
assert(isstruct(overrides) && isscalar(overrides), 'overrides must be a scalar struct.');

base = struct( ...
    'caseType', 'poiseuille', ...
    'boundary_left', 'periodic', ...
    'boundary_right', 'periodic', ...
    'boundary_bottom', 'thermalize', ...
    'boundary_top', 'thermalize', ...
    'bodyForceX', 0.1, ...
    'g', 0.0, ...
    'Utop', 0.0, ...
    'Ubottom', 0.0, ...
    'wallSigma', 1.0, ...
    'useThermostat', true, ...
    'keepMeanFlow', false, ...
    'gamma', 20.0, ...
    'Nx', 50, ...
    'Ny', 50, ...
    'Lx', 10.0, ...
    'Ly', 10.0, ...
    'dt', 5e-3, ...
    'alphaDeg', 170.0, ...
    'alpha', deg2rad(170.0), ...
    'kBT', 1.0, ...
    'useIncompressibleRedistribution', true, ...
    'redistribAfterCollision', true, ...
    'coef', 0.1, ...
    'highMode', 'coef', ...
    'lowMode', 'coef', ...
    'maxRedistribPasses', 2, ...
    'enableMomentumCorrectionPostRedistribution', true, ...
    'useLocalFluidFractionThresholds', false, ...
    'fluidFracGain', 0.8, ...
    'lowThrFloor', 20, ...
    'highThrFloor', 25, ...
    'lowThrBulkOverride', NaN, ...
    'useZoneRedistribution', true, ...
    'zoneTileNx', 25, ...
    'zoneTileNy', 25, ...
    'zoneUseShiftedSecondPass', true, ...
    'zonePassthroughMode', false, ...
    'zoneSingleFullDomainMode', false, ...
    'useLiquidClosure', true, ...
    'useOptimalBetaRepair', true, ...
    'betaRepair', 0.0, ...
    'betaEOS', 1.0, ...
    'Kvirial', 75.0, ...
    'smoothPdriveAtInterzone', true, ...
    'smoothPdriveInterzoneWidthCells', 2, ...
    'smoothPdriveInterzoneBlend', 1.0, ...
    'smoothPdriveIncludeShiftedLayouts', true, ...
    'noSurfaceCase', true, ...
    'redistributionEnableSurfaceTopology', false, ...
    'redistributionWallWettingEnabled', false, ...
    'useInterfaceVelocityReorientation', false, ...
    'benchmark_nSteps', 2000, ...
    'benchmark_metricsEvery', 1, ...
    'benchmark_dumpSteps', '0,100,300,600,900,1200,1500,1800,2000');

merged = merge_structs_local(base, overrides);
params = build_tranche5_params(merged);

% Benchmark extras kept explicitly on the returned struct.
if isfield(overrides, 'benchmark_nSteps')
    params.benchmark_nSteps = overrides.benchmark_nSteps;
else
    params.benchmark_nSteps = base.benchmark_nSteps;
end
if isfield(overrides, 'benchmark_metricsEvery')
    params.benchmark_metricsEvery = overrides.benchmark_metricsEvery;
else
    params.benchmark_metricsEvery = base.benchmark_metricsEvery;
end
if isfield(overrides, 'benchmark_dumpSteps')
    params.benchmark_dumpSteps = overrides.benchmark_dumpSteps;
else
    params.benchmark_dumpSteps = base.benchmark_dumpSteps;
end
end

function s = merge_structs_local(a,b)
s = a;
fn = fieldnames(b);
for k = 1:numel(fn)
    s.(fn{k}) = b.(fn{k});
end
end
