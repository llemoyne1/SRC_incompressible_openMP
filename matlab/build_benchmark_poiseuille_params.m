function params = build_benchmark_poiseuille_params(overrides)
%BUILD_BENCHMARK_POISEUILLE_PARAMS Fixed params for Poiseuille 1500-step benchmark.
if nargin < 1 || isempty(overrides)
    overrides = struct();
end
base = struct( ...
    'boundary_left', 'periodic', ...
    'boundary_right', 'periodic', ...
    'boundary_bottom', 'thermalize', ...
    'boundary_top', 'thermalize', ...
    'bodyForceX', 0.05, ...
    'g', 0.0, ...
    'Utop', 0.0, ...
    'Ubottom', 0.0, ...
    'useThermostat', true, ...
    'keepMeanFlow', false, ...
    'gamma', 10.0, ...
    'Nx', 50, ...
    'Ny', 50, ...
    'Lx', 10.0, ...
    'Ly', 10.0, ...
    'dt', 5e-3, ...
    'alpha', deg2rad(170.0), ...
    'kBT', 1.0, ...
    'zoneTileNx', 16, ...
    'zoneTileNy', 16, ...
    'zoneUseShiftedSecondPass', true, ...
    'zonePassthroughMode', false, ...
    'zoneSingleFullDomainMode', false, ...
    'useZoneRedistribution', true, ...
    'useLiquidClosure', true, ...
    'smoothPdriveAtInterzone', true, ...
    'smoothPdriveInterzoneWidthCells', 1, ...
    'smoothPdriveInterzoneBlend', 1.0, ...
    'benchmark_nSteps', 1500, ...
    'benchmark_metricsEvery', 1, ...
    'benchmark_dumpSteps', '0,100,300,600,900,1200,1500');
params = build_tranche5_params(merge_structs_local(base, overrides));
% keep benchmark extras
if isfield(overrides,'benchmark_nSteps'), params.benchmark_nSteps = overrides.benchmark_nSteps; else, params.benchmark_nSteps = base.benchmark_nSteps; end
if isfield(overrides,'benchmark_metricsEvery'), params.benchmark_metricsEvery = overrides.benchmark_metricsEvery; else, params.benchmark_metricsEvery = base.benchmark_metricsEvery; end
if isfield(overrides,'benchmark_dumpSteps'), params.benchmark_dumpSteps = overrides.benchmark_dumpSteps; else, params.benchmark_dumpSteps = base.benchmark_dumpSteps; end
end

function s = merge_structs_local(a,b)
s = a;
fn = fieldnames(b);
for k=1:numel(fn)
    s.(fn{k}) = b.(fn{k});
end
end
